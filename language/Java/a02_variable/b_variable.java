package a02_variable;

public class b_variable {
    public static void main(String[] args) {
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32,768 ~ 32,767
        int i = 2147483647;
        long l = 9223372036854775807L;

        double d = 9.223372036854775807;
        float f = 10.0f;

        /*
         * long 은 뒤에 L을 붙이고 float 는 f 를 붙인다
         */ 
    }
}
